<h2 class="op">Your CCPA Rights to Make Certain Requests</h2>
<p class="op">If you are a California resident, you may request that we disclose to you the following information covering the 12 months preceding your request:</p>
<ol class="op">
<li class="op">the categories of Personal Information we collected about you and the categories of sources from which we collected such Personal Information;</li>
<li class="op">the business or commercial purpose for collecting Personal Information about you;</li>
<li class="op">the categories of Personal Information about you that we disclosed or sold to third parties for a business purpose and the categories of third parties to whom we disclosed or sold Personal Information (if applicable);</li>
<li class="op">the specific pieces of Personal Information we collected about you.</li>
</ol>
<p class="op">
</p><p class="op">If you are a California resident, you may also request that we delete Personal Information we collected from you.</p>
<p class="op">Finally, if you are a California resident, you have the right to opt-out of the sale of your Personal Information.</p>
<p class="op">We may decline to honor your request, in full or part, for certain reasons, such as a legal exception. For example, if the information you are requesting is subject to certain federal privacy laws, like the GLBA or HIPAA, that are outside the scope of the CCPA rights requests, we may decline to honor your request. We also may decline to honor your access or deletion request if we cannot verify your identity or confirm that the Personal Information that we maintain relates to you, or if we cannot verify that you have the authority to make a request on behalf of another individual.</p>
<p class="op">You have the right to be free from unlawful discrimination for exercising your rights under the CCPA.</p>
